#!/usr/bin/env python3
"""Build provider-neutral distribution artifacts from the skill sources.

Generates two files in dist/:

- founder-wisdom-full.md — SKILL.md plus every reference file concatenated
  into one self-contained document. The Claude-specific YAML frontmatter is
  replaced with a plain-prose "When to use this document" section (derived
  from the frontmatter description, so it stays in sync), and every
  file-based cross-link (`references/foo.md`, `foo.md`) is rewritten as a
  section reference, since the bundle has sections, not files.
- system-prompt.md — a one-page system-prompt stub for platforms where the
  corpus lives in a retrieval store rather than in context (e.g. a ChatGPT
  Custom GPT with file upload).

The sources in SKILL.md and references/ remain the single source of truth;
never edit dist/ by hand. Run: python3 scripts/build_bundle.py
"""

import re
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
REFS = ROOT / "references"
DIST = ROOT / "dist"

GENERATED_NOTE = (
    "<!-- Generated by scripts/build_bundle.py — do not edit by hand.\n"
    "     Edit SKILL.md and references/*.md in the source repository instead,\n"
    "     then rerun: python3 scripts/build_bundle.py -->\n"
)


def fail(msg: str) -> None:
    sys.exit(f"build_bundle: {msg}")


def replace_once(text: str, old: str, new: str, where: str) -> str:
    """Replace a string that must occur exactly once, failing loudly if the
    source has drifted so a rewrite silently stops applying."""
    count = text.count(old)
    if count != 1:
        fail(f"expected exactly one occurrence of {old!r} in {where}, found {count}")
    return text.replace(old, new)


# Per-file rewrites for phrasings the generic passes in build_bundle() can't
# handle. Applied with replace_once so a reworded source breaks the build
# instead of silently leaking stale text into the bundle.
REF_REWRITES = {
    "socratic-technique.md": [
        (
            "SKILL.md carries a short excerpt of this table",
            "The overview at the top of this document carries a short excerpt of this table",
        ),
        (
            "SKILL.md says this skill is not for reassurance",
            "The overview at the top of this document says this corpus is not for reassurance",
        ),
    ],
    "capital-valuation.md": [
        (
            "trusting a number in a reference file",
            "trusting a number in a reference document",
        ),
    ],
}


def parse_skill() -> tuple[str, str]:
    """Return (description, body) from SKILL.md."""
    raw = (ROOT / "SKILL.md").read_text(encoding="utf-8")
    m = re.match(r"\A---\n(.*?)\n---\n(.*)\Z", raw, flags=re.DOTALL)
    if not m:
        fail("SKILL.md frontmatter not found")
    frontmatter, body = m.group(1), m.group(2)
    dm = re.search(r"^description: (.+)$", frontmatter, flags=re.MULTILINE)
    if not dm:
        fail("description field not found in SKILL.md frontmatter")
    return dm.group(1).strip(), body.lstrip("\n")


def load_titles() -> dict[str, str]:
    """Map each reference filename to its H1 title."""
    titles = {}
    for path in sorted(REFS.glob("*.md")):
        first = path.read_text(encoding="utf-8").splitlines()[0]
        if not first.startswith("# "):
            fail(f"{path.name} does not start with an H1 title")
        titles[path.name] = first[2:].strip()
    return titles


def section_ref(name: str, titles: dict[str, str]) -> str:
    # Drop a leading "The" so refs read "the “YC Canon” section",
    # not "the “The YC Canon” section".
    title = re.sub(r"^The ", "", titles[name])
    return f'the “{title}” section'


def rewrite_file_links(text: str, titles: dict[str, str], where: str) -> str:
    """Rewrite backticked file pointers as section references."""

    def sub(m: re.Match) -> str:
        name = m.group(1)
        if name not in titles:
            fail(f"{where} links to unknown reference file {name!r}")
        return section_ref(name, titles)

    return re.sub(r"`(?:references/)?([a-z0-9-]+\.md)`", sub, text)


def build_skill_section(body: str, titles: dict[str, str]) -> tuple[str, list[str]]:
    """Transform the SKILL.md body for the bundle and return it along with
    the reference-file order taken from the domain routing list."""
    order = []
    for name in re.findall(r"^- `references/([a-z0-9-]+\.md)` — ", body, flags=re.MULTILINE):
        if name in order:
            fail(f"duplicate routing entry for {name}")
        order.append(name)
    on_disk = set(titles)
    if set(order) != on_disk:
        missing = sorted(on_disk - set(order))
        extra = sorted(set(order) - on_disk)
        fail(f"routing list out of sync with references/ (missing: {missing}, extra: {extra})")

    # The routing list becomes a table of contents over sections.
    body = re.sub(
        r"^- `references/([a-z0-9-]+\.md)` — ",
        lambda m: f"- **{titles[m.group(1)]}** — ",
        body,
        flags=re.MULTILINE,
    )

    # File-reading instructions become section-consulting instructions.
    body = replace_once(
        body,
        "The corpus is organized by **domain** in the `references/` directory. "
        "Read only the files relevant to the situation — don't load all of them. "
        "The current domains:",
        "The corpus is organized by **domain** — each domain is a section later "
        "in this document. Consult only the sections relevant to the situation "
        "— don't try to apply all of them at once. The current domains:",
        "SKILL.md",
    )
    body = replace_once(
        body,
        "read 2–3 files. Don't read more than that",
        "consult 2–3 sections. Don't go broader than that",
        "SKILL.md",
    )
    body = replace_once(
        body,
        "The finance files (`finance-ops.md`, `capital-valuation.md`)",
        "The finance sections (“Finance & Operations”, “Capital & Valuation”)",
        "SKILL.md",
    )

    # Everything else that points at a file becomes a section reference.
    body = rewrite_file_links(body, titles, "SKILL.md")
    return body, order


def build_bundle(description: str, body: str, order: list[str], titles: dict[str, str]) -> str:
    when_to_use = description.replace("this skill", "this document")
    body = replace_once(
        body,
        "# Founder Wisdom\n",
        "# Founder Wisdom\n\n"
        "*This is the complete single-file bundle of the Founder Wisdom corpus, "
        "for use with any large-context LLM (ChatGPT, Gemini, or others). It is "
        "generated from the [Founder-Wisdom](https://github.com/Zachgraff/Founder-Wisdom) "
        "repository; the guidance below and the domain sections that follow "
        "are the entire corpus in one document.*\n\n"
        "## When to use this document\n\n"
        f"{when_to_use}\n",
        "SKILL.md",
    )

    # The bundle is a standalone document, not a Claude skill — rewrite the
    # skill framing. Backstopped by the post-build check on "this skill".
    body = (
        body
        .replace("A reference skill", "A reference document")
        .replace("The skill operates", "The document operates")
        .replace("this skill", "this document")
        .replace("This skill", "This document")
        .replace("the skill itself", "the document itself")
    )

    parts = [GENERATED_NOTE, body.rstrip() + "\n"]
    for name in order:
        content = (REFS / name).read_text(encoding="utf-8")
        for old, new in REF_REWRITES.get(name, []):
            content = replace_once(content, old, new, name)
        # Files become sections in the bundle, so self-descriptions like
        # "This file covers…" are rewritten to match. Backstopped by the
        # post-build checks on file/skill framing.
        content = (
            content
            .replace("This file", "This section")
            .replace("this file", "this section")
            .replace("Companion file:", "Companion section:")
            .replace("file in this corpus", "section in this corpus")
            .replace("this skill", "this document")
            .replace("This skill", "This document")
        )
        content = rewrite_file_links(content, titles, name)
        parts.append("\n---\n\n" + content.rstrip() + "\n")
    bundle = "\n".join(parts)
    # A section reference that opens a sentence needs a capital.
    bundle = re.sub(r"(?m)^the “", "The “", bundle)
    bundle = bundle.replace(". the “", ". The “")
    return bundle


def build_system_prompt(
    description: str, skill_section: str, order: list[str], titles: dict[str, str]
) -> str:
    # Drop the description's opening summary sentence ("Surfaces hard-won
    # axioms…") — the stub's own intro covers it — and keep the trigger
    # conditions from "Use this skill whenever…" onward.
    marker = ". Use this skill whenever"
    if marker not in description:
        fail(f"expected {marker!r} in SKILL.md description")
    triggers = (
        "Use the knowledge files whenever"
        + description.split(marker, 1)[1]
    ).replace("this skill", "the knowledge files")

    # The section roster is derived from the routing list — which the bundle
    # build has already sync-checked against references/ — so a renamed or
    # added reference file can never leave this list stale. Each entry keeps
    # its routing hints: the "Consult for/whenever…" sentences and any
    # trailing italic cross-routing note.
    entries = dict(re.findall(r"(?m)^- \*\*(.+?)\*\* — (.+)$", skill_section))
    if set(entries) != {titles[name] for name in order}:
        fail("system prompt: routing bullets out of sync with reference titles")
    roster_lines = []
    for name in order:
        title = titles[name]
        hints = re.findall(r"Consult (?:for|whenever)[^.]*\.?", entries[title])
        note = re.search(r"\*\((.+?)\)\*\s*$", entries[title])
        if note:
            hints.append(note.group(1))
        hints = [h.strip().rstrip(".") + "." for h in hints]
        roster_lines.append(f"- {title}" + (" — " + " ".join(hints) if hints else ""))
    roster = "\n".join(roster_lines)

    sm = re.search(r"(?ms)^## How to stage-match\n+(.*?)(?=^## )", skill_section)
    if not sm:
        fail("system prompt: '## How to stage-match' section not found in SKILL.md")
    stage_matching = sm.group(1).strip()

    ex1, ex2 = (titles[name] for name in order[:2])
    return f"""{GENERATED_NOTE}
# Founder Wisdom — system prompt for retrieval-based deployments

Use this as the system prompt / custom instructions on platforms where the
Founder Wisdom corpus is uploaded as knowledge files rather than pasted into
context (e.g. a ChatGPT Custom GPT or Project with file upload). For
large-context platforms, prefer pasting `founder-wisdom-full.md` directly.

---

You are a thinking partner for startup founders and operators, grounded in a
curated corpus of hard-won axioms in your knowledge files. {triggers}

Your knowledge files are organized as named topic sections. Because retrieval
returns fragments, search the knowledge files for **whole sections by name** —
e.g. "the {ex1} section" or "the {ex2} section" — rather than relying only on
what surfaces for the raw query, and search again if the retrieved material
seems thin or off-topic. The sections, with routing notes where a topic could
land in more than one place:

{roster}

When a question spans multiple domains (and most real founder questions do),
retrieve 2–3 sections. Don't go broader than that unless explicitly asked for
a comprehensive scan.

You operate in two modes:

- **Direct mode (default).** When someone asks a clear question, surface the
  relevant axioms: lead with the axiom, then the reasoning, then the qualifier.
- **Socratic mode.** When someone is processing a live decision, helping someone
  else with a founder-stage decision (advisor, coach, mentor), or working
  through ambivalence rather than seeking information, don't hand them the
  axiom — ask the question the axiom answers. One question at a time; wait for
  the answer before the next. When this mode is active, retrieve the Socratic
  Technique section for the full axiom-to-question table and questioning craft.

How to stage-match:

{stage_matching}

Output style:

- Pull 3–7 axioms maximum per response, in **bolded axiom + explanation**
  format. The axiom itself should be quotable in one sentence.
- Don't over-hedge; founders need conviction. When something genuinely is
  contextual, say so plainly and explain the dependency. Don't moralize.
- When relevant, name the limit of the axiom ("fire fast — except never in
  anger, never on a Friday").
- Stage-match: a pre-seed founder needs different axioms than a Series C CEO.
  Follow the stage-matching rules above: skip axioms whose *(Stage: …)* tag
  doesn't match; untagged axioms hold at every stage.
- Benchmarks age; axioms don't. Cite any figure tagged *[bench YYYY-MM]* with
  its vintage, never as timeless.
- Attribute when relevant ("Paul Graham's 'do things that don't scale'").

Do not give detailed legal, tax, or specific financial advice — give the
mechanics and vocabulary, then defer to qualified professionals. Do not offer
reassurance the corpus argues against: if a founder wants a bad idea blessed,
surface the axiom that pushes back. The corpus is opinionated and reflects a
particular slice of the startup world (largely U.S., venture-backed, software);
name geographic, sectoral, and structural exceptions when relevant. And apply
the corpus's own meta-axiom to itself: most advice is contextual — treat it as
data, not instruction.
"""


def main() -> None:
    description, body = parse_skill()
    titles = load_titles()
    skill_section, order = build_skill_section(body, titles)
    bundle = build_bundle(description, skill_section, order, titles)
    system_prompt = build_system_prompt(description, skill_section, order, titles)

    # Post-build sanity checks (skip the generated-file header comment, which
    # legitimately points back at the source layout).
    bundle_body = bundle.split("-->", 1)[1]
    if "references/" in bundle_body:
        fail("bundle still contains a dangling references/ path")
    for name, title in titles.items():
        h1 = f"# {title}"
        if sum(1 for line in bundle.splitlines() if line == h1) != 1:
            fail(f"expected exactly one {h1!r} heading in bundle ({name})")
    leftover = re.findall(r"`[a-z0-9-]+\.md`", bundle)
    if leftover:
        fail(f"bundle still contains file-style links: {sorted(set(leftover))}")
    if re.search(r"(?i)this skill|SKILL\.md", bundle_body):
        fail("bundle still contains skill framing or a bare SKILL.md mention")
    if re.search(r"(?i)\b(this|reference) files?\b", bundle_body):
        fail("bundle still contains file framing (expected sections/document)")

    DIST.mkdir(exist_ok=True)
    (DIST / "founder-wisdom-full.md").write_text(bundle, encoding="utf-8")
    (DIST / "system-prompt.md").write_text(system_prompt, encoding="utf-8")
    print(f"wrote dist/founder-wisdom-full.md ({len(bundle):,} bytes, {len(order)} sections)")
    print(f"wrote dist/system-prompt.md ({len(system_prompt):,} bytes)")


if __name__ == "__main__":
    main()
